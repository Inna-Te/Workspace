public class HW1c {

    public static void main(String[] args) {
        // Write a Java program with a 5 decimal variables. Multiply all 5 of them and print results in the following format:
        // number a * number b *.... equals <result>

        double num = 10.2;
        double num1 = 11.8;
        double num2 = 2.589;
        double num3 = 74.65;
        double num4 = 114.4;

        double result = (double) num * num1 * num2 * num3 * num4;

        System.out.println(num + " *  " + num1 + " * " + num2 + " * " + num3 + " * " + num4 + " = " + result);



    }
}
