package homework3;


public class Modul3b {

    public static void main(String[] args) {

        int a = 3, b = 6;
        int sum = 0;

        for (int i = a; i <= b; i++) {
            sum = sum + i;
        }

        System.out.println("The summary between 2 numbers:" + a + " " + "and" + " " + b + " " + "is" + " " + sum);
    }
}

